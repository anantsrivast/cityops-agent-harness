        "    \"the task required, (b) every write it claims is confirmed by the database evidence,\\n\"\n"
        "    \"and (c) the final answer is consistent with the trajectory. Otherwise verified=false.\\n\"\n"
        "    \"Be strict: an answer that merely SOUNDS complete does not pass.\\n\\n\"\n"
        "    \"# Task\\n{task}\\n\\n# Trajectory\\n{trajectory}\\n\\n# Final answer\\n{answer}\\n\\n\"\n"
        "    \"# Database evidence\\n{evidence}\"\n"
        ")\n"
        "\n"
        "\n"
        "def build_evidence(trajectory):\n"
