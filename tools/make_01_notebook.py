        "            fid = step[\"result\"].strip()\n"
        "            with conn.cursor() as cur:\n"
        "                cur.execute(\"\"\"SELECT asset_id, category, severity FROM CITY_INSPECTION_FINDING\n"
        "                               WHERE finding_id = :f\"\"\", f=fid)\n"
        "                row = cur.fetchone()\n"
        "            if row:\n"
        "                lines.append(f\"finding {fid} EXISTS: asset={row[0]} category={row[1]} severity={row[2]}\")\n"
        "            else:\n"
        "                lines.append(f\"finding {fid} NOT FOUND in CITY_INSPECTION_FINDING\")\n"
        "    return \"\\n\".join(lines) or \"(no database writes claimed)\"\n"
